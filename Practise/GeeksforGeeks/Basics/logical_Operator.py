#showing logical operation
#or
print(True or False)

#and
print(True and False)

#Not
print(not True)

#Using "in" to check
if 's' in 'geeksforgeeks':
    print("S is a part of geeksforgeeks")
else:
    print("S is not a part of geeksforgeeks")

# using "in" to loop through
for i in 'geeksforgeeks':
    print(i, end=" ")

print("\r")

#Using is to check object identity for a string
# is immutable ( Cannot be changed once allocated
print(' ' is ' ')

#Using is to check object identity for dict
#dict is mutable(can be changed once allocated
print({} is {})

